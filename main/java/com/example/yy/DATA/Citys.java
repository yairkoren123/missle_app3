package com.example.yy.DATA;

public class Citys {
    public String city;
    public String data;


    public Citys(String city, String data) {
        this.city = city;
        this.data = data;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public Citys() {
    }
}
